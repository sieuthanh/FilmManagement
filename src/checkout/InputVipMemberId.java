	private void jButton1ActionPerformed(java.awt.event.ActionEvent evt)
			throws WriteException, IOException {// GEN-FIRST:event_jButton1ActionPerformed
		// TODO add your handling code here:
		String id = jTextField1.getText();
		String name = jTextField2.getText();
		String email = jPasswordField1.getText();
		String message = "";
		if (id == null || id.equals("")) {
			message = message + "Id field must not be empty\n";
		}
		if (name == null || name.equals("")) {
			message = message + "Name field must not be empty\n";
		}
		if (email == null || email.equals("")) {
			message = message + "Message field must not be empty\n";
		}
		if (!message.equals("")) {
			JOptionPane.showMessageDialog(null, message);
		} else {
			HibernateUtil hu = new HibernateUtil();
			SessionFactory sf = hu.getSessionFactory();
			Session session = sf.openSession();
			Criteria criteria = session.createCriteria(VipMember.class);
			criteria.add(Restrictions.eq("id", id));
			// criteria.add(Restrictions.eq("name", name));
			// criteria.add(Restrictions.eq("email", email));
			VipMember vipMember = (VipMember) criteria.uniqueResult();
			//session.beginTransaction().commit();
			session.close();
			if (vipMember == null) {
				JOptionPane.showMessageDialog(null,
						"You are not vip member. You can not check out");
				setVisible(false);
				dispose();
			} else {

				WriteExcel we = new WriteExcel(billElementList);
				we.setOutputFile("d:/bill.xls");
				we.write();
				JOptionPane.showMessageDialog(null, "Check out complete");
				setVisible(false);
				dispose();
				ArrayList<CartElement> cartElementList = MainWindow.cartClick
						.getCp().getCartElementList();
				ArrayList<Disk> diskDatabase = new ArrayList<Disk>();
				session = sf.openSession();
				criteria = session.createCriteria(Disk.class);
				Disjunction or = Restrictions.disjunction();
				for (int i = 0; i < cartElementList.size(); i++) {
					or.add(Restrictions.eq("filmId", cartElementList.get(i)
							.getFilmId()));
				}
				criteria.add(or);
				diskDatabase = (ArrayList<Disk>) criteria.list();
				int totalAmount;
				for (int i = 0; i < diskDatabase.size(); i++) {
					totalAmount = Integer.parseInt(diskDatabase.get(i)
							.getTotalAmount())
							- cartElementList.get(i).getDiskAmount();
					if (totalAmount > 0) {
						diskDatabase.get(i).setTotalAmount(totalAmount + "");
					} else {
						diskDatabase.get(i).setTotalAmount("0");
					}
					session.saveOrUpdate(diskDatabase.get(i));
				}
				int sql = session.createSQLQuery("delete from cartelement").executeUpdate();
				session.beginTransaction().commit();
				session.close();
				MainWindow.cartClick.setVisible(false);
				MainWindow.cartClick.dispose();
			}

		}
	}// GEN-LAST:event_jButton1ActionPerformed


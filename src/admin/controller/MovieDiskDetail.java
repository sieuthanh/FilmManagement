package admin.controller;

public class MovieDiskDetail {
	private String name;
	private String diskPerMovie;
	private String diskAmount;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDiskPerMovie() {
		return diskPerMovie;
	}

	public void setDiskPerMovie(String diskPerMovie) {
		this.diskPerMovie = diskPerMovie;
	}

	public String getDiskAmount() {
		return diskAmount;
	}

	public void setDiskAmount(String diskAmount) {
		this.diskAmount = diskAmount;
	}
}
